<script lang="ts">
  import { onMount } from 'svelte';
  import { apiClient } from '$lib/api/client';
  import type { DockerContainer } from '$lib/api/client';
  import { Package, Play, Square, RotateCcw } from 'lucide-svelte';
  import { fade } from 'svelte/transition';

  let containers: DockerContainer[] = [];
  let loading = true;
  let error: string | null = null;
  let selectedContainer: DockerContainer | null = null;

  const loadContainers = async () => {
    try {
      containers = await apiClient.getDockerContainers(true);
      error = null;
    } catch (err) {
      error = 'Failed to load Docker containers';
      console.error('Docker error:', err);
    } finally {
      loading = false;
    }
  };

  const handleContainerAction = async (container: DockerContainer, action: 'start' | 'stop' | 'restart') => {
    try {
      await apiClient.dockerContainerAction(container.id, action);
      await loadContainers();
    } catch (err) {
      console.error(`Failed to ${action} container:`, err);
    }
  };

  const getStatusColor = (status: DockerContainer['status']) => {
    switch (status) {
      case 'running': return '#22c55e';
      case 'exited': return '#ef4444';
      case 'paused': return '#f59e0b';
      case 'restarting': return '#3b82f6';
      default: return '#6b7280';
    }
  };

  onMount(async () => {
    await loadContainers();
    // Refresh every 10 seconds
    setInterval(loadContainers, 10000);
  });
</script>


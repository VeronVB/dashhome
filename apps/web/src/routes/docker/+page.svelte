            </div>
            {#if container.ports.length > 0}
              <div class="info-row">
                <span class="info-label">Ports:</span>
                <div class="ports-list">
                  {#each container.ports as port}
                    <span class="port-badge">
                      {port.publicPort ? `${port.publicPort}:` : ''}{port.privatePort}/{port.type}
                    </span>
                  {/each}
                </div>
              </div>
            {/if}
          </div>

          <div class="container-actions">
            {#if container.status !== 'running'}
              <button
                class="action-btn action-btn-start"
                on:click={() => handleContainerAction(container, 'start')}
                disabled={container.status === 'restarting'}
              >
                <Play size={16} />
                Start
              </button>
            {/if}
            
            {#if container.status === 'running'}
              <button
                class="action-btn action-btn-stop"
                on:click={() => handleContainerAction(container, 'stop')}
              >
                <Square size={16} />
                Stop
              </button>
            {/if}
            
            <button
              class="action-btn action-btn-restart"
              on:click={() => handleContainerAction(container, 'restart')}
              disabled={container.status === 'restarting'}
            >
              <RotateCcw size={16} />
              Restart
            </button>
          </div>
        </div>
      {/each}
    </div>
  {/if}

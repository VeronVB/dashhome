      </div>
      <CircularProgress value={stats.disk.percentage} max={100} size={60} />
      <div class="stat-value">{stats.disk.percentage}%</div>
      <div class="stat-subtitle">{stats.disk.used}GB / {stats.disk.total}GB</div>
    </div>

    <div class="stat-item">

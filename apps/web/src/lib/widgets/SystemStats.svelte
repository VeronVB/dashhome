<script lang="ts">
  import { onMount, onDestroy } from 'svelte';
  import { apiClient } from '$lib/api/client';
  import type { SystemStats } from '$lib/api/client';
  import { Activity, Cpu, HardDrive, Thermometer } from 'lucide-svelte';
  import CircularProgress from '$lib/components/UI/CircularProgress.svelte';

  export let widgetId: string;

  let stats: SystemStats | null = null;
  let loading = true;
  let error: string | null = null;
  let interval: NodeJS.Timeout;

  const loadStats = async () => {
    try {
      stats = await apiClient.getSystemStats();
      error = null;
    } catch (err) {
      error = 'Failed to load system stats';
      console.error('System stats error:', err);
    } finally {
      loading = false;
    }
  };

  onMount(async () => {
    await loadStats();
    // Refresh every 5 seconds
    interval = setInterval(loadStats, 5000);
  });

  onDestroy(() => {
    if (interval) {
      clearInterval(interval);
    }
  });

  const formatBytes = (bytes: number): string => {
    const sizes = ['B', 'KB', 'MB', 'GB', 'TB'];
    if (bytes === 0) return '0 B';
    const i = Math.floor(Math.log(bytes) / Math.log(1024));
    return Math.round(bytes / Math.pow(1024, i) * 100) / 100 + ' ' + sizes[i];
  };

  const formatUptime = (seconds: number): string => {
    const days = Math.floor(seconds / 86400);
    const hours = Math.floor((seconds % 86400) / 3600);
    const minutes = Math.floor((seconds % 3600) / 60);
    
    if (days > 0) {
      return `${days}d ${hours}h ${minutes}m`;
    } else if (hours > 0) {
      return `${hours}h ${minutes}m`;
    } else {
      return `${minutes}m`;
    }
  };
</script>

{#if loading}
  <div class="loading-container">

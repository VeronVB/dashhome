          <span class="temp-value">{stats.temp.value}°{stats.temp.unit}</span>
        {:else}
          <span class="temp-value">N/A</span>
        {/if}
      </div>
      <div class="stat-subtitle">Uptime: {formatUptime(stats.uptime)}</div>
    </div>
  </div>

  <div class="system-info">
    <div class="info-row">
